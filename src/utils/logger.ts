import pino from 'pino'

export type Logger = typeof logger

/**
 * Создание логгера с использованием библиотеки Pino.
 * Настроен для вывода структурированных сообщений в консоль с цветовым форматированием.
 *
 * Уровни логирования (от низшего к высшему):
 * - trace: детальная трассировка выполнения
 * - debug: отладочная информация
 * - info: информационные сообщения
 * - warn: предупреждения
 * - error: ошибки
 * - fatal: критические ошибки
 *
 * Текущий уровень установлен на 'debug', что означает, что будут выводиться
 * сообщения уровня debug и выше.
 */
export const logger = pino({
  // Уровень логирования (например, debug, info, warn, error)
  level: 'debug',
  transport: {
    // Использование пакета pino-pretty для форматирования логов
    target: 'pino-pretty',
    options: {
      // Включение цветного вывода для удобства чтения
      colorize: true
    }
  }
})
